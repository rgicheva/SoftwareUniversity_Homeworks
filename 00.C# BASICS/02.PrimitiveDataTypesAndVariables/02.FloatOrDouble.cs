using System;

class FloatOrDouble
{
    static void Main()
    {
        double doubleA = 34.567839023;
        Console.WriteLine("The value of the double variable A is {0}", doubleA);
        float doubleB = 12.345f;
        Console.WriteLine("The value of the float variable B is {0}", doubleB);
        double doubleC = 8923.1234857;
        Console.WriteLine("The value of the double variable C is {0}", doubleC);
        float floatD = 3456.091f;
        Console.WriteLine("The value of the float variable D is {0}", floatD);
    }
}

