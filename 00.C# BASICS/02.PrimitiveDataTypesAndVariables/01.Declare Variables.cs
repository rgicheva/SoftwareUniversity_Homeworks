using System;

class DeclareVariables
{
    static void Main()
    {
        ushort a = 52130;
        Console.WriteLine("The value of a is {0} ", a);
        sbyte b = -115;
        Console.WriteLine("The value of a is {0} ", b);
        uint c = 4825932;
        Console.WriteLine("The value of a is {0} ", c);
        byte d = 97;
        Console.WriteLine("The value of a is {0} ", d);
        short e = -10000;
        Console.WriteLine("The value of a is {0} ", e);
    }
}

